package com.example.travel360.model;

public class Vehicle {

        private String namere;
        private String addressre;
        private Integer ctnumber;
        private String rentId;

    public Vehicle() {
    }

    public String getNamere() {
        return namere;
    }

    public void setNamere(String namere) {
        this.namere = namere;
    }

    public String getAddressre() {
        return addressre;
    }

    public void setAddressre(String addressre) {
        this.addressre = addressre;
    }

    public Integer getCtnumber() {
        return ctnumber;
    }

    public void setCtnumber(Integer ctnumber) {
        this.ctnumber = ctnumber;
    }

    public String getRentId() {
        return rentId;
    }

    public void setRentId(String rentId) {
        this.rentId = rentId;
    }
}
